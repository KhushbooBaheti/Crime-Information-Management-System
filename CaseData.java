package com.demo.example;

public class CaseData {
	private String case_id,date,place,des_crime,victims,detailed_description,suspect,p_station;

	public String getCase_id() {
		return case_id;
	}

	public void setCase_id(String case_id) {
		this.case_id = case_id;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getPlace() {
		return place;
	}

	public void setPlace(String place) {
		this.place = place;
	}

	public String getDes_crime() {
		return des_crime;
	}

	public void setDes_crime(String des_crime) {
		this.des_crime = des_crime;
	}

	public String getVictims() {
		return victims;
	}

	public void setVictims(String victims) {
		this.victims = victims;
	}

	public String getDetailed_description() {
		return detailed_description;
	}

	public void setDetailed_description(String detailed_description) {
		this.detailed_description = detailed_description;
	}

	public String getSuspect() {
		return suspect;
	}

	public void setSuspect(String suspect) {
		this.suspect = suspect;
	}

	public String getP_station() {
		return p_station;
	}

	public void setP_station(String p_station) {
		this.p_station = p_station;
	}

}
